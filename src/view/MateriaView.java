import controller.AlunoController;
import controller.EscolaController;
import controller.MateriaController;
import controller.ProfessorController;
import model.Aluno;
import model.Escola;
import model.Materia;
import model.Professor;

import java.io.IOException;
import java.util.List;
import java.util.Optional;
import java.util.Scanner;

public abstract class MateriaView {
    public static void menu(Scanner in) {
        System.out.println("\n>>> Entrou no menu de MAT√âRIAS <<<");

        List<Materia> materias;
        List<Escola> escolas;

        try {
            materias = MateriaController.carregar();
            escolas = EscolaController.carregar();
        } catch (Exception e) {
            System.err.println("Erro ao carregar dados: " + e.getMessage());
            return;
        }

        MateriaController materiaController = new MateriaController(materias);
        EscolaController escolaController = new EscolaController(escolas);

        boolean loop = true;
        while (loop) {
            System.out.println("\nüìò --- GERENCIAMENTO DE MAT√âRIAS ---");
            System.out.println("1 - Cadastrar Mat√©ria");
            System.out.println("2 - Listar Mat√©rias");
            System.out.println("3 - Editar Mat√©ria");
            System.out.println("4 - Deletar Mat√©ria");
            System.out.println("0 - Voltar");
            System.out.print("Op√ß√£o: ");
            String opcao = in.nextLine().trim();

            switch (opcao) {
                case "1" -> {
                    if (escolaController.listarEscolas().isEmpty()) {
                        System.out.println("‚ö†Ô∏è Nenhuma escola cadastrada. Cadastre uma escola antes.");
                        break;
                    }

                    System.out.print("Nome da mat√©ria: ");
                    String nomeMateria = in.nextLine().trim();
                    if (nomeMateria.isBlank()) {
                        System.out.println("‚ö†Ô∏è O nome da mat√©ria n√£o pode ser vazio.");
                        break;
                    }

                    System.out.println("üîó Escolas dispon√≠veis:");
                    escolaController.listarEscolas().forEach(
                            e -> System.out.println("ID: " + e.getId() + " | Nome: " + e.getNome()));

                    System.out.print("Digite o ID da escola vinculada: ");
                    String idEscolaStr = in.nextLine().trim();
                    if (idEscolaStr.isBlank()) {
                        System.out.println("‚ö†Ô∏è ID da escola n√£o pode ser vazio.");
                        break;
                    }

                    try {
                        int idEscola = Integer.parseInt(idEscolaStr);
                        Optional<Escola> escolaOpt = escolaController.buscarEscolaPorId(idEscola);

                        if (escolaOpt.isPresent()) {
                            materiaController.cadastrarMateria(nomeMateria, escolaOpt.get());
                            System.out.println("‚úÖ Mat√©ria cadastrada com sucesso!");
                        } else {
                            System.out.println("‚ùå Escola com ID " + idEscola + " n√£o encontrada.");
                        }
                    } catch (NumberFormatException e) {
                        System.out.println("‚ùå ID inv√°lido.");
                    }
                }

                case "2" -> {
                    List<Materia> lista = materiaController.listarMaterias();
                    if (lista.isEmpty()) {
                        System.out.println("üì≠ Nenhuma mat√©ria cadastrada.");
                    } else {
                        System.out.println("üìö Lista de Mat√©rias:");
                        lista.forEach(System.out::println);
                    }
                }

                case "3" -> {
                    List<Materia> lista = materiaController.listarMaterias();
                    if (lista.isEmpty()) {
                        System.out.println("‚ö†Ô∏è Nenhuma mat√©ria cadastrada. N√£o √© poss√≠vel editar.");
                        break;
                    }

                    lista.forEach(System.out::println);

                    System.out.print("ID da mat√©ria: ");
                    String idStr = in.nextLine().trim();
                    if (idStr.isBlank()) {
                        System.out.println("‚ö†Ô∏è ID n√£o pode ser vazio.");
                        break;
                    }

                    try {
                        int id = Integer.parseInt(idStr);
                        Optional<Materia> materiaOpt = materiaController.buscarMateriaPorId(id);

                        if (materiaOpt.isEmpty()) {
                            System.out.println("‚ùå Mat√©ria com ID " + id + " n√£o encontrada.");
                            break;
                        }

                        Materia materia = materiaOpt.get();

                        System.out.print("Novo nome (pressione ENTER para manter): ");
                        String novoNome = in.nextLine().trim();
                        if (!novoNome.isBlank()) {
                            materia.setNomeMateria(novoNome);
                        }

                        System.out.print("Deseja alterar a escola vinculada? (s/n): ");
                        String resposta = in.nextLine().trim().toLowerCase();
                        if (resposta.equals("s")) {
                            List<Escola> listaEscolas = escolaController.listarEscolas();
                            if (listaEscolas.isEmpty()) {
                                System.out.println("‚ö†Ô∏è Nenhuma escola cadastrada.");
                                break;
                            }

                            listaEscolas.forEach(
                                    e -> System.out.println("ID: " + e.getId() + " | Nome: " + e.getNome()));

                            System.out.print("ID da nova escola: ");
                            String idEscolaStr = in.nextLine().trim();
                            if (idEscolaStr.isBlank()) {
                                System.out.println("‚ö†Ô∏è ID da escola n√£o pode ser vazio.");
                                break;
                            }

                            try {
                                int idEscola = Integer.parseInt(idEscolaStr);
                                Optional<Escola> escolaOpt = escolaController.buscarEscolaPorId(idEscola);
                                if (escolaOpt.isPresent()) {
                                    materia.setEscola(escolaOpt.get());
                                    System.out.println("üè´ Escola vinculada atualizada com sucesso.");
                                } else {
                                    System.out.println("‚ùå Escola com ID " + idEscola + " n√£o encontrada.");
                                }
                            } catch (NumberFormatException e) {
                                System.out.println("‚ùå ID inv√°lido.");
                            }
                        }

                        System.out.println("‚úèÔ∏è Mat√©ria atualizada com sucesso!");
                    } catch (NumberFormatException e) {
                        System.out.println("‚ùå ID inv√°lido.");
                    }
                }

                case "4" -> {
                    if (materiaController.listarMaterias().isEmpty()) {
                        System.out.println("‚ö†Ô∏è Nenhuma mat√©ria cadastrada. N√£o √© poss√≠vel deletar.");
                        break;
                    }

                    materiaController.listarMaterias().forEach(System.out::println);

                    System.out.print("ID da mat√©ria: ");
                    String idStr = in.nextLine().trim();
                    if (idStr.isBlank()) {
                        System.out.println("‚ö†Ô∏è ID n√£o pode ser vazio.");
                        break;
                    }

                    try {
                        int id = Integer.parseInt(idStr);
                        List<Professor> professores = ProfessorController.carregar();
                        List<Aluno> alunos = AlunoController.carregar();

                        boolean deletada = materiaController.deletarMateria(id, professores, alunos);
                        if (deletada) {
                            System.out.println("üóëÔ∏è Mat√©ria deletada com sucesso.");
                        } else {
                            System.out.println("‚ùå A mat√©ria est√° vinculada a algum professor ou aluno.");
                        }

                    } catch (NumberFormatException e) {
                        System.out.println("‚ùå ID inv√°lido.");
                    } catch (Exception e) {
                        System.err.println("Erro ao carregar professores/alunos: " + e.getMessage());
                    }
                }

                case "0" -> loop = false;

                default -> System.out.println("‚ùå Op√ß√£o inv√°lida! Tente novamente.");
            }

            try {
                materiaController.salvar();
            } catch (IOException e) {
                System.err.println("Erro ao salvar mat√©rias: " + e.getMessage());
            }
        }
    }
}
